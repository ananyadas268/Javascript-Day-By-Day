// Variables in JavaScript

let age = 20; // can change
const name = "Ananya"; // cannot change

age = 21; // valid
// name = "Das"; ❌ error

console.log(age, name);

/*
Explanation:
- let → used when value changes
- const → used when value is fixed
*/
// Note: Always use const by default, and only use let when you know the value will change.