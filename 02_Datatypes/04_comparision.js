//comparision
let a = 10;
let b = 20;
console.log(a > b) //false
console.log(a < b) //true
console.log(a >= b) //false
console.log(a <= b) //true
console.log(a == b) //false
console.log(a != b) //true
//different data types
console.log(1 == "1") //true
console.log(1 === "1") //false
//== only checks value
//=== checks value and data type